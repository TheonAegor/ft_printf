#include "../includes/libft.h"

void		ft_putnbr_u(unsigned x, int fd)
{
	if (x < 10)
	{
		x += 48;
		write(fd, &x, 1);
	}
	else
	{
		ft_putnbr_u(x/10, fd);
		ft_putnbr_u(x%10, fd);
	}
	return ;
}
